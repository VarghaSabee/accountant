    /**
     * Завантаження даних в таблицю
     */
    private void load() {
        DefaultTableModel model = (DefaultTableModel) statniTable.getModel();
        model.setRowCount(0);
        for (int i = 0; i < StatniyObj.nazvPosadList.size(); i++) {
            model.addRow(new Object[]{
                StatniyObj.nazvPosadList.get(i),
                StatniyObj.posOkladList.get(i),
                StatniyObj.rangHRNLIst.get(i),
                StatniyObj.vislHRNLIst.get(i),
                StatniyObj.fondZPList.get(i),});
        }

    }

    private boolean checkCorrect() {
        if (isDouble(stavkaTXt.getText())) {
            return true;
        }
        return false;
    }


