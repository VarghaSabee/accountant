    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        if (statniTable.getRowCount() < 21) {
            if (checkCorrect()) {
                if (!VidomistForm.isContain(StatniyObj.nazvPosadList, posadaTxt.getSelectedItem().toString())) {
                    StatniyObj.addObj(posadaTxt.getSelectedItem().toString(), Double.parseDouble(stavkaTXt.getText()),
                            rangTxt.getSelectedItem().toString(), visluhaTxt.getSelectedItem().toString());
                    load();
                } else {
                    JOptionPane.showMessageDialog(null, "Посада не може повторюватися!", "Помилка", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(null, "Не вдалося додати", "Помилка", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Максимальна кількість рядків - 20!", "Помилка", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_addBtnActionPerformed

    private void delBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delBtnActionPerformed
        int index = statniTable.getSelectedRow();
        if (index >= 0) {
            StatniyObj.remove(index);
            load();
        } else {
            JOptionPane.showMessageDialog(null, "Виберіть рядок!", "Інформація", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_delBtnActionPerformed

    private void delAllBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delAllBtnActionPerformed
        DefaultTableModel model = (DefaultTableModel) statniTable.getModel();
        model.setRowCount(0);
        StatniyObj.clear();
        load();
    }//GEN-LAST:event_delAllBtnActionPerformed

