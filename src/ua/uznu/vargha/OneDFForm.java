    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        if (oneDFTbl.getRowCount() < 51) {
            if (checkCorrect()) {
                if (!VidomistForm.isContain(OneDFObj.idenList, Long.parseLong(idenTxt.getText()))) {
                    OneDFObj.addObj(Long.parseLong(idenTxt.getText()), Double.parseDouble(sumTxt.getText()), Integer.parseInt(oznakaCombo.getSelectedItem().toString()));
                    load();
                } else {
                    JOptionPane.showMessageDialog(null, "Ідентифікаційний код \nне може повторюватися!", "Помилка", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(null, "Не вдалося додати!", "Помилка", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Максимальна кількість рядків - 50!", "Помилка", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_addBtnActionPerformed

    private void delBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delBtnActionPerformed
        int index = oneDFTbl.getSelectedRow();
        if (index >= 0) {
            OneDFObj.idenList.remove(index);
            OneDFObj.sumhDohodList.remove(index);
            OneDFObj.sumUtrymane.remove(index);
            OneDFObj.oznakaList.remove(index);
            load();
        } else {
            JOptionPane.showMessageDialog(null, "Виберіть рядок!", "Інформація", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_delBtnActionPerformed

    private void delAllBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delAllBtnActionPerformed
        DefaultTableModel model = (DefaultTableModel) oneDFTbl.getModel();
        model.setRowCount(0);
        OneDFObj.idenList.clear();
        OneDFObj.sumhDohodList.clear();
        OneDFObj.sumUtrymane.clear();
        OneDFObj.oznakaList.clear();
        load();
    }//GEN-LAST:event_delAllBtnActionPerformed

