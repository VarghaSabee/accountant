    /**
     * Завантаження даних в таблицю
     */
    private void load() {
        DefaultTableModel model = (DefaultTableModel) oneDFTbl.getModel();
        model.setRowCount(0);
        for (int i = 0; i < OneDFObj.idenList.size(); i++) {
            model.addRow(new Object[]{
                OneDFObj.idenList.get(i),
                OneDFObj.sumhDohodList.get(i),
                OneDFObj.sumhDohodList.get(i),
                OneDFObj.sumUtrymane.get(i),
                OneDFObj.sumUtrymane.get(i),
                OneDFObj.oznakaList.get(i)
            });
        }

    }

    /**
     * Перевірка правильністю введених даних
     *
     * @return логічний
     */
    private boolean checkCorrect() {
        if (idenTxt.getText().length() == 10
                && isLong(idenTxt.getText()) && isDouble(sumTxt.getText())) {
            return true;
        }
        return false;
    }


