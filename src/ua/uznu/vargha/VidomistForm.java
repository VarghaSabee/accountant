    /**
     * Завантаження даних в таблицю
     */
    private void load() {
        DefaultTableModel model = (DefaultTableModel) vidomTbl.getModel();
        model.setRowCount(0);
        for (int i = 0; i < VidomistObj.nameList.size(); i++) {
            model.addRow(new Object[]{
                VidomistObj.nameList.get(i),
                VidomistObj.idList.get(i),
                VidomistObj.narahList.get(i),
                VidomistObj.podatokList.get(i),
                VidomistObj.vPodatokList.get(i),
                VidomistObj.profSpList.get(i)});
        }

    }

    /**
     * Перевіка на довгий число
     *
     * @param s число
     * @return логічний
     */
    public static boolean isLong(String s) {
        try {
            Long.parseLong(s);
            return true;
        } catch (Exception e) {
            return false;
        }
    }

    /**
     * Перевіка на дійсне число
     *
     * @param s число
     * @return логічний
     */
    public static boolean isDouble(String s) {
        try {
            Double.parseDouble(s);
            if (Double.parseDouble(s) >= 0) {
                return true;
            } else {
                return false;
            }
        } catch (Exception e) {
            return false;
        }
    }

    /**
     * Перевірка правільності введених даних
     *
     * @return логічний
     */
    private boolean checkCorrect() {
        if (pibTxt.getText().length() > 5 && idenTxt.getText().length() == 10
                && isLong(idenTxt.getText()) && isDouble(narahTxt.getText())) {
            return true;
        }
        return false;
    }

    /**
     * Порівняння об'єктів
     *
     * @param str1 об'єкт 1
     * @param str2 об'єкт 2
     * @return логічний
     */
    public static boolean comparison(Object str1, Object str2) {
        if (str1.equals(str2)) {
            return true;
        } else {
            return false;
        }
    }

    /**
     * Масив містить такого елемента
     *
     * @param list масив
     * @param matches елемент
     * @return логічний
     */
    public static boolean isContain(List list, Object matches) {
        for (int i = 0; i < list.size(); i++) {
            if (comparison(matches, list.get(i))) {
                return true;
            }
        }
        return false;
    }

