    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        if (vidomTbl.getRowCount() < 36) {
            if (checkCorrect()) {
                if (!isContain(VidomistObj.idList, Long.parseLong(idenTxt.getText()))) {
                    VidomistObj.addObj(pibTxt.getText(), Long.parseLong(idenTxt.getText()), Double.parseDouble(narahTxt.getText()));
                    load();
                } else {
                    JOptionPane.showMessageDialog(null, "Ідентифікаційний код \nне може повторюватися!", "Помилка", JOptionPane.ERROR_MESSAGE);
                }

            } else {
                JOptionPane.showMessageDialog(null, "Не вдалося додати!", "Помилка", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Максимальна кількість рядків - 35!", "Помилка", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_addBtnActionPerformed

    private void delBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delBtnActionPerformed
        int index = vidomTbl.getSelectedRow();
        if (index >= 0) {
            VidomistObj.nameList.remove(index);
            VidomistObj.idList.remove(index);
            VidomistObj.narahList.remove(index);
            VidomistObj.podatokList.remove(index);
            VidomistObj.vPodatokList.remove(index);
            VidomistObj.profSpList.remove(index);
            VidomistObj.vsohoList.remove(index);
            load();
        } else {
            JOptionPane.showMessageDialog(null, "Виберіть рядок!", "Інформація", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_delBtnActionPerformed

